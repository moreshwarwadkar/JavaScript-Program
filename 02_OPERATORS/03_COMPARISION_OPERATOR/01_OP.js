//Comparision Operator

let x = 10;
let y = 5;
let z = "10";

console.log(x == z);   // true  (same value)
console.log(x === z);  // false (different type)
console.log(x != y);   // true  (values not equal)
console.log(x > y);    // true
console.log(x < y);    // false
console.log(x >= y);   // true
console.log(x <= y);   // false
