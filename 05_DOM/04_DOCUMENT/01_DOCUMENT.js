/*
1) document.write()
-> When we use document.write(), it will not give any extra space or line between the two outputs.
*/
document.write("Hello,");
document.write("World!");   // Hello,World!

/*
2) document.writeln()
-> When we use document.writeln(), it show signle space between the two outputs.
*/
document.writeln("Line 1");
document.writeln("Line 2");  // Line 1 Line 2