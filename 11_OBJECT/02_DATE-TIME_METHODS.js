// OBJECT METHODS

// DATE & TIME 

let ref = new Date();

console.log(ref);  // Tue Nov 11 2025 19:53:37 GMT+0530 (India Standard Time)
console.log(ref.getFullYear());  // 25
console.log(ref.getMonth());  // 10
console.log(ref.getDay());  // 2

console.log(ref.getHours());  // 19
console.log(ref.getMinutes());  // 55
console.log(ref.getSeconds());  // 56
console.log(ref.getMilliseconds());  // 126

console.log(ref.toLocaleDateString());  // 11/11/2025
console.log(ref.toLocaleTimeString());  // 7:57:15 pm




